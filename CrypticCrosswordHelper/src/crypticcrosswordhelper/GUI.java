    private static ArrayList historyItems = new <String>ArrayList();

    public static void updateHistory() {
        historyItems.clear();
        for (int i=0; i<Main.getCurrentCrossword().getNoClues(); i++) {
            Clue thisClue = Main.getCurrentCrossword().getClue(i);
            String item = thisClue.getLocation() + ": \"" + thisClue.getOrigClue() + "\" (" + thisClue.getMaskLength() + ")";
            historyItems.add(item);
        }
        jList5.removeAll();
        jList5.setListData(historyItems.toArray());
    }

